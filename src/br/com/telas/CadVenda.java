    public final void Ultimo() {

        try {
            ControllerVenda venda = new ControllerVenda();
            ResultSet rs = venda.ultimaVenda();
            while (rs.next()) {
                String n = Integer.toString(rs.getInt(1));
                txtId.setText(n);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    public void incluir(ModelAuxiliar get) {

        this.setVisible(true);
        tableModel.addRow(get);
        setPrecoQtd();

    }

    public void cliente(ModelPessoa cli) {

        String idcli = Integer.toString(cli.getIdPessoa());
        String nomeCli = cli.getPessoanome();
        this.setVisible(true);
        txtCliente.setText(nomeCli);
        setPrecoQtd();

    }

    public void setPrecoQtd() {
        double TotValor = 0;
        int TotQtd = 0;
        for (int i = 0; i < tableModel.getRowCount(); i++) {
            int qtd = Integer.parseInt((String) tableModel.getValueAt(i, 2));
            double valor = Double.parseDouble((String) tableModel.getValueAt(i, 4));
            TotQtd += qtd;
            TotValor += valor;
            String TotValorStr = String.valueOf(TotValor);
            String TotQtdStr = String.valueOf(TotQtd);
            txtValorTotal.setText(TotValorStr);
            txtQuant.setText(TotQtdStr);
        }
             
    }

