    private void adicionarFunc() throws SQLException {
        try {
            ModelFuncionario func = new ModelFuncionario();

            ControllerFuncionario contFunc = new ControllerFuncionario(this);

            func.setPessoanome(txtNomef.getText());
            func.setPessoaCPF(txtCpff.getText());
            func.setPessoaEmail(txtEmailf.getText());
            func.setPessoaTele(txtTelff.getText());
            func.setRua(txtEnderecof.getText());
            func.setNumero(txtNumf.getText());
            func.setBairro(txtBairrof.getText());
            func.setCep(txtCepf.getText());
            func.setIdCidade(i);
            func.setCargo(txtCargof.getText());
            func.setSalario(txtSalariof.getText());

            contFunc.adicionarFun(func);
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    Vector<Integer> IdCidade = new Vector<>();

    public void DadosCbx() {
        try {
            ControllerCidade cidade = new ControllerCidade();
            ResultSet rs = cidade.listarCidades();

            while (rs.next()) {
                IdCidade.addElement(rs.getInt(1));
                CbxCidadef.addItem(rs.getString(2));
            }
        } catch (SQLException e) {

        }
    }

    public void Ultimo() {
        try {
            ControllerFuncionario person = new ControllerFuncionario(this);
            ResultSet rs = person.ultimoElemento();
            while (rs.next()) {
                int n = rs.getInt(1);
                String str = Integer.toString(n);
                txtIdf.setText(str);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

