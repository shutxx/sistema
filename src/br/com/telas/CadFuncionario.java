    private void CbxCidadefActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CbxCidadefActionPerformed
        // TODO add your handling code here:
        try {
            ControllerCidade cidad = new ControllerCidade();
            ResultSet rs = cidad.listarCidades();

            while (rs.next()) {
                String city = rs.getString(2);
                if (city.equals(CbxCidadef.getSelectedItem())) {
                    txtUFf.setText(rs.getString(3));
                    i = rs.getInt(1);
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_CbxCidadefActionPerformed

